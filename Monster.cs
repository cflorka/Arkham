namespace Arkham
{
	public class Monster
	{
		public int sneak, horrorCheck, sanityDam, fight, damage, toughness;
		
		public Monster(int sneak, int horrorCheck, int sanityDam, int fight, int damage, int toughness)
		{
			this.sneak = sneak;
			this.horrorCheck = horrorCheck;
			this.sanityDam = sanityDam;
			this.fight = fight;
			this.damage = damage;
			this.toughness = toughness;
		}
	}
}
