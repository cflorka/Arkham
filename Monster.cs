namespace Arkham
{
	public class Monster
	{
		public int sneak, horror, sanity, fight, damage, toughness;
		
		public Monster(int sneak, int horror, int sanity, int fight, int damage, int toughness)
		{
			this.sneak = sneak;
			this.horror = horror;
			this.sanity = sanity;
			this.fight = fight;
			this.damage = damage;
			this.toughness = toughness;
		}
	}
}
